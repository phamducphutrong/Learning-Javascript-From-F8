/*
Toán tử số học
+      --> Cộng
-      --> Trừ
*      --> Nhân
**     --> Lũy thừa
/      --> Chia
%      --> Chia lấy số dư
++     --> Tăng 1 giá trị
--     --> Giảm 1 giá trị
*/

var a = 1;
var b = 2;
var c = a + b; // Cộng
var c = a - b; // Trừ
var c = a * b; // Nhân
var c = a ** b; // Lũy thừa
var c = a / b; // Chia
var c = a % b; // Chia lấy phần dư
a++; // Tăng 1 giá trị
b--; // Giảm 1 giá trị
console.log(c);
console.log(a);
console.log(b);