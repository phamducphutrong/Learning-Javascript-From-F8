/*
Toán tử gán

Toán tử     Ví dụ      Tương đương
   =        x = y       x = y
  +=        x += y      x = x + y
  -=        x -= y      x = x - y
  *=        x *= y      x = x * y
  /=        x /= y      x = x / y
 **=        x **= y     x = x ** y
*/

var a = 1; // Toán tử gán
a += 2;
console.log(a);